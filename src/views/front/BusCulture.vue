美国历史上由公交车引发的黑人民权运动

1955年12月1日，在美国蒙哥马利市的公交车上，一位黑人裁缝拒绝给白人让座，随后遭到警察逮捕，该事件激起了当地黑人的愤怒，他们强烈抗议公交车上的种族隔离政策。

种族隔离制度曾在美国十分普遍，有些州的法律明确规定，黑人与白人在公交车、餐馆等公共场所内需要分隔。例如，在公交车上，黑人和白人的座位是被前后分隔开来，黑人必须给白人让座；大部分座位是白人座位，只有后面少数几排是黑人座位；即使白人座位空着，黑人也不能坐，只能站在后面。因此，部分黑人宁愿打车，也不坐受白人歧视的公交车。

为了改变社会地位的不平等，黑人们推选26岁的牧师马丁·路德·金为抗议运动领导人。他举行了一次正式会议，50位黑人牧师及各行业代表到场，他们分析了当前形势、预测了抗议运动的后果，于是，大家决定，在12月5日周一发起“拒搭公交车行动”。这一行动的消息通过教堂扩散，所有黑人给予了积极响应。

这场行动以非暴力抵抗的方式持续了381天，最终，美国最高法院宣布阿拉巴马州的种族隔离法律违反宪法，蒙哥马利市公交车上的种族隔离规定也被废除。马丁·路德·金也由此成为黑人争取自由和平等权利运动的领袖，随后，越来越多的黑人挺身而出，黑人民权运动如火如荼地开展起来，并在美国南方形成高潮。
